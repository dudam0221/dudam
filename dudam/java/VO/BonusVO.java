public class BonusVO extends CmmnVO {
	
	private String empno;
	private String job;
	private int sal;
	private int comm;
	
	private List<SalgradeVO> result;
	private SalgradeVO result1;
	
	public String getEmpno() {
		return empno;
	}
	public void setEmpno(String empno) {
		this.empno = empno;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}
	public int getComm() {
		return comm;
	}
	public void setComm(int comm) {
		this.comm = comm;
	}

	public List<SalgradeVO> getResult() {
		return result;
	}
	public void setResult(List<SalgradeVO> result) {
		this.result = result;
	}

	public SalgradeVO getResult1() {
		return result1;
	}
	public void setResult1(SalgradeVO result1) {
		this.result1 = result1;
	}
	
}
